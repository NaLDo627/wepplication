package com.wepplication.Form;

public class MorrisLogForm {
    private String time;
    private Integer imageEditor;
    private Integer videoEditor;
    private Integer gifEditor;
    private Integer encryption;
    private Integer ssh;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Integer getImageEditor() {
        return imageEditor;
    }

    public void setImageEditor(Integer imageEditor) {
        this.imageEditor = imageEditor;
    }

    public Integer getVideoEditor() {
        return videoEditor;
    }

    public void setVideoEditor(Integer videoEditor) {
        this.videoEditor = videoEditor;
    }

    public Integer getGifEditor() {
        return gifEditor;
    }

    public void setGifEditor(Integer gifEditor) {
        this.gifEditor = gifEditor;
    }

    public Integer getEncryption() {
        return encryption;
    }

    public void setEncryption(Integer encryption) {
        this.encryption = encryption;
    }

    public Integer getSsh() {
        return ssh;
    }

    public void setSsh(Integer ssh) {
        this.ssh = ssh;
    }
}
